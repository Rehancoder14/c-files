#include <iostream>
using namespace std;

int main() {
    int a = 5, b = 7;
    float c = 3.7, d = 8.0;
    cout<<a<<" + "<<b<<" = "<<a+b<<endl;
    cout<<a<<" + "<<c<<" = "<<a+c<<endl;
    cout<<c<<" + "<<d<<" = "<<c+d<<endl;
    cout<<b<<" / "<<c<<" = "<<a/b<<endl;
    cout<<d<<" * "<<b<<" = "<<b*d<<endl;
    cout<<c<<" - "<<b<<" = "<<a-b<<endl;
    
 

    return 0 ;
}